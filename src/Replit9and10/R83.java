package Replit9and10;

public class R83 {
    public static void main(String[] args) {


        int[][] num = {{1, 2, 3}, {4, 5, 6}};

        int sum1 = 0;
        int sum2 = 0;

//sum for first row
        for (int a = 0; a == 0; a++) {
            for (int b = 0; b <= 2; b++) {
                sum1 += num[a][b];
            }
            System.out.println(sum1);
        }

//sum for second row
        for (int a = 1; a == 1; a++) {
            for (int b = 0; b <= 2; b++) {
                sum2 += num[a][b];
            }
            System.out.println(sum2);
        }
    }
}