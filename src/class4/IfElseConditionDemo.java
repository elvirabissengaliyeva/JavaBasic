package class4;

public class IfElseConditionDemo {
    public static void main(String[] args) {
        int number1=10;
        int number2=10;

        if(number1==number2){
            System.out.println("Equal");
        }
        else if(number1>number2){
            System.out.println("Number1 greater");
        }
        else if(number1<number2){
            System.out.println("Number2 greater");
        }

    }

}
